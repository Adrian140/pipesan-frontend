// helper + hook neschimbate
const getDict = (lang = 'fr', ns = 'nav') =>
  (dictionaries[lang] && dictionaries[lang][ns]) ||
  (dictionaries.fr && dictionaries.fr[ns]) ||
  {};

export const useTranslation = (ns = 'nav') => {
  const { currentLanguage } = useLanguage();
  const dict = getDict(currentLanguage, ns);
  const t = (key) => (key in dict ? dict[key] : key);
  return { t, dict, lang: currentLanguage };
};

export const translate = (lang, ns, key) => {
  const d = getDict(lang, ns);
  return key in d ? d[key] : key;
};

export default dictionaries;
